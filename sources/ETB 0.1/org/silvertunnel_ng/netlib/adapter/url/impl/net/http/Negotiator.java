package org.silvertunnel_ng.netlib.adapter.url.impl.net.http;

import java.io.IOException;
import java.lang.reflect.Constructor;














































































































































































































































































































abstract class Negotiator
{
  Negotiator() {}
  
  static Negotiator getSupported(String hostname, String scheme)
    throws Exception
  {
    Class clazz = Class.forName("sun.net.www.protocol.http.NegotiatorImpl");
    Constructor c = clazz.getConstructor(new Class[] { String.class, String.class });
    
    return (Negotiator)c.newInstance(new Object[] { hostname, scheme });
  }
  
  abstract byte[] firstToken()
    throws IOException;
  
  abstract byte[] nextToken(byte[] paramArrayOfByte)
    throws IOException;
}
