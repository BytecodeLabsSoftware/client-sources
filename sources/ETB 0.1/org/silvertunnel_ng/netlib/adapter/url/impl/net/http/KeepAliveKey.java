package org.silvertunnel_ng.netlib.adapter.url.impl.net.http;

import java.net.URL;
















































































































































































































































































































































































































class KeepAliveKey
{
  private String protocol = null;
  private String host = null;
  private int port = 0;
  private Object obj = null;
  






  public KeepAliveKey(URL url, Object obj)
  {
    protocol = url.getProtocol();
    host = url.getHost();
    port = url.getPort();
    this.obj = obj;
  }
  




  public boolean equals(Object obj)
  {
    if (!(obj instanceof KeepAliveKey))
    {
      return false;
    }
    KeepAliveKey kae = (KeepAliveKey)obj;
    
    return (host.equals(host)) && (port == port) && (protocol.equals(protocol)) && (this.obj == obj);
  }
  





  public int hashCode()
  {
    String str = protocol + host + port;
    
    return obj == null ? str.hashCode() : str.hashCode() + obj.hashCode();
  }
}
