package org.silvertunnel_ng.netlib.layer.tor.util;



















public class TorException
  extends Exception
{
  private static final long serialVersionUID = 597691078427388360L;
  


















  public TorException() {}
  

















  public TorException(String message)
  {
    super(message);
  }
  




  public TorException(Throwable cause)
  {
    super(cause);
  }
  




  public TorException(String message, Throwable cause)
  {
    super(message, cause);
  }
}
